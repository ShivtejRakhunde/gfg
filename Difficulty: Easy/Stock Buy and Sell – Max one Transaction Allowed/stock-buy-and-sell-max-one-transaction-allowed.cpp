class Solution {
  public:
    int maximumProfit(vector<int> &prices) {
        // code here
        int minp = prices[0];
        int maxP = 0;
        for(int i=1; i<prices.size(); i++){
            if(minp<prices[i]){
                maxP = max(maxP, prices[i]-minp);
            }
            minp = min(minp, prices[i]);
        }
        return maxP;
    }
};


